package ex_08_If_Condition;

public class Ex_04_pre_increment {

    public static void main(String[] args) {
        int a=20;

        if(a<5)
        {
            ++a;
        }
        System.out.println(a);

        int x = 4;

        if (++x == 5) {
            System.out.println("x is 5 now"); // This will print
        }


    }
}
